function renderProduct(value) {
    if (Array.isArray(value) && value.length) {
        let card = ''
        value.map(element => {
            card += `<div class="produk-card">
    <img src="${element.image}" alt="" style="width: 100%;">
    <div class="title">
        <div class="nama">${element.title}</div>
        <div class="harga">Rp. ${element.price}</div>
    </div>
</div>`
        })
        document.getElementsByClassName('list-produk')[0].innerHTML = card;
    } else {
        let card = ''
        valuesCards.map(element => {
            card += `<div class="produk-card">
    <img src="${element.image}" alt="" style="width: 100%;">
    <div class="title">
        <div class="nama">${element.title}</div>
        <div class="harga">Rp. ${element.price}</div>
    </div>
</div>`
        })
        document.getElementsByClassName('list-produk')[0].innerHTML = card;
    }
}



function filterChange() {
    let card = []
    Array.from(document.querySelectorAll('.tab-filter input[value]:checked'), function (input) {
        const attrib = input.getAttribute('value');
        let filtered = valuesCards.filter(function (el) {
            return el.category == attrib;
        })
        filtered.forEach(element => {
            card.push(element)
        });
    })
    renderProduct(card)
}

