        <h2>Pemesanan Roti</h2>
        <p>
            Selain menyediakan roti, kue tradisional, dan kue tart untuk penjualan secara retail di outlet-outlet kami,
            LaRasa Bakery juga menerima pemesanan roti, kue tradisional, dan kue tart untuk event spesial Anda. LaRasa
            Bakery telah berpengalaman untuk menyediakan produk dalam jumlah besar untuk berbagai acara dengan tetap
            mempertahankan standar kualitas LaRasa Bakery.
        </p>
        <p>
            Pemesanan dapat dilakukan dengan cara datang ke salah satu outlet kami maupun melalui hotline telepon. Anda
            bebas memilih untuk mengambil pesanan Anda di salah satu outlet LaRasa. Selain itu, dengan minimal nilai
            pemesanan tertentu, kami dapat mengirimkan pesanan langsung ke tempat Anda.
        </p>
        <p>
            Alasan mengapa memesan roti di LaRasa Bakery merupakan pilihan terbaik bagi Anda:
        </p>

        <ul id="layanan-list">
            <li>
                <b>Sering Menangani Pesanan Dalam Jumlah Sangat Besar</b><br>
                Kapasitas produksi LaRasa Bakery dapat memenuhi pesanan hingga 35.000 roti dalam satu hari. Pemesanan
                dapat dilakukan paling lambat 24 jam (1 hari) sebelum waktu pengambilan/ pengiriman pesanan.
            <li>
                <b>Produk Selalu Baru & Segar</b><br>
                Kami menjumpai banyak produsen roti yang menyimpan stock roti selama lebih dari 1 hari ketika mereka
                menerima pesanan dalam jumlah besar. Hal ini dilakukan karena kapasita produksi yang tidak memadai.
                Tapi, LaRasa Bakery melakukan yang lebih baik untuk Anda. Seluruh roti pesanan di LaRasa Bakery dijamin
                baru karena diproduksi kurang dari 24 jam dari waktu pengambilan/ pengiriman pesanan.
            </li>
            <li>
                <b>Pilihan Produk dan Harga Beragam</b><br>
                LaRasa Bakery menyediakan lebih dari 243 varian produk utama mulai dari harga Rp.3.000,-. Dengan
                tersedianya beragam pilihan produk, Anda lebih bebas memilih varian produk yang sesuai untuk event
                spesial Anda.
            </li>
            <li>
                <b>Gratis Kemasan Eksklusif</b><br>
                Harga produk kami sudah meliputi kemasan (box) eksklusif sehingga tampak mewah ketika Anda menyajikannya
                di acara spesial Anda. Selain itu, kami dapat menyediakan kemasan (box) dengan desain khusus untuk
                pesanan dalam jumlah tertentu (tanpa biaya tambahan).
            </li>
            <li>
                <b>Halal</b><br>
                Seluruh produk LaRasa Bakery sudah disertifikasi halal oleh MUI (Majelis Ulama Indonesia).
            </li>
        </ul>
