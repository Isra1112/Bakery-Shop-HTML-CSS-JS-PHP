                <b>Promo Khusus Member Tiap Bulan</b><br>
                Kami selalu menyedian promo khusus untuk member setiap bulan. Dengan promo yang beragam member LaRasa
                akan selalu mendapat keistimewaan dan manfaat lebih ketika berbelanja di LaRasa Bakery. Promo tersedia
                untuk penjualan secara retail (langsung membeli di outlet kami) maupun untuk pemesanan. Anda dapat
                mengikuti informasi promo yang berlaku bulan ini dengan memantau halaman informasi Promo LaRasa,dengan
                Like halaman resmi Facebook LaRasa, atau Follow Instagram LaRasa.
            </li>
            <li>
                <b>Diskon 40% untuk Pembelian Kue Tart</b><br>
                Setiap member LaRasa berhak atas diskon 40% untuk pembelian kue tart. Diskon tersebut dapat digunakan
                untuk pembelian kue tart baik bagi member yang bersangkutan maupun untuk diberikan pada kerabat. Diskon
                khusus ini dapat digunakan sebanyak 1 kali selama setahun.
            </li>
            <li>
                <b>Point untuk Tiap Transaksi</b><br>
                LaRasa memberikan 1 point untuk setiap Rp.2.500,- yang dibelanjakan member LaRasa. Akumulasi point
                tersebut dapat ditukarkan dengan berbagai merchandise LaRasa.
            </li>
        </ul>
